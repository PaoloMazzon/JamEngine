#include "JamError.h"

/////////////////////////////////////////////////////////
void jSetError(uint16 errorCode) {
	jErrorCode = errorCode | jErrorCode;
}
/////////////////////////////////////////////////////////

/////////////////////////////////////////////////////////
uint16 jGetError() {
	uint16 oldErrors = jErrorCode;
	jErrorCode = 0;
	return oldErrors;
}
/////////////////////////////////////////////////////////