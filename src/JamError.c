#include "JamError.h"

/////////////////////////////////////////////////////////
void jSetError(uint8 errorCode) {
	jErrorCode = errorCode | jErrorCode;
}
/////////////////////////////////////////////////////////

/////////////////////////////////////////////////////////
uint8 jGetError() {
	uint8 oldErrors = jErrorCode;
	jErrorCode = 0;
	return oldErrors;
}
/////////////////////////////////////////////////////////